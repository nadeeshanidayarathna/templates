const { group } = require("yargs");
const base = require("../../../common/base");

const scraper = async function download(url, sp, path) {
    try {
        console.log("callings scraper url:" + url + " sp:" + sp + " path:" + path);
        const browser = await base.puppeteer().launch({
            headless: false,
            devtools: false
        });
        {
            // 1.Download
            console.log("getting url:" + url);
            const page = await browser.newPage();
            await base.downloadPage(page, url, sp, path);

            // 2.Identify
            // 3.Wrap
            await page.waitForSelector(".pub");
            await page.evaluate(function process() {
                // #############
                // # root:info #
                // #############
                const rootTitle = "Pre-existing condition provisions in group and blanket disability policies";
                wrapElementLevel1(document, rootTitle);
                wrapElementDate(document, "issue-date", "2007-12-14" + "T00:00:00");

                // ################
                // # content:info #
                // ################
                const contentElement = document.querySelector(".pub");
                wrapElement(document, contentElement, "ease-content");

                const footNotes = document.querySelectorAll('.cltext > p:last-of-type')[0];
                let i = 0;
                let footnoteElement = [];
                for (const footNote of footNotes.childNodes) {
                    if (i > 1) {
                        footnoteElement.push(footNote);
                    }i++;
                }
                wrapElements(document, footnoteElement, "footnote", group=true);


                return Promise.resolve();
            });
            // 4.Write
            await base.writePage(page, url, sp, path, true, true, false);
            await page.close();
        }
        await base.test().runPageTest(browser, url, sp, path);
        await browser.close();
    } catch (e) {
        console.log(e);
        console.log("Failed to scrape!!!");
        process.exit(1);
    }
}
module.exports = scraper;